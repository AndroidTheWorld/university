package sj.db.bean;

public class Book {
	private String Book_id;
	private String Book_name;
	private String Book_author;
	private double Book_price;
	private String Book_type;
	private String Book_isbn;
	private String Book_publisher;
	private int Book_allnum;
	private int Book_surplus;
	public String getBook_id() {
		return Book_id;
	}
	public void setBook_id(String book_id) {
		Book_id = book_id;
	}
	public String getBook_name() {
		return Book_name;
	}
	public void setBook_name(String book_name) {
		Book_name = book_name;
	}
	public String getBook_author() {
		return Book_author;
	}
	public void setBook_author(String book_author) {
		Book_author = book_author;
	}
	public double getBook_price() {
		return Book_price;
	}
	public void setBook_price(double book_price) {
		Book_price = book_price;
	}
	public String getBook_type() {
		return Book_type;
	}
	public void setBook_type(String book_type) {
		Book_type = book_type;
	}
	public String getBook_isbn() {
		return Book_isbn;
	}
	public void setBook_isbn(String book_isbn) {
		Book_isbn = book_isbn;
	}
	public String getBook_publisher() {
		return Book_publisher;
	}
	public void setBook_publisher(String book_publisher) {
		Book_publisher = book_publisher;
	}
	public int getBook_allnum() {
		return Book_allnum;
	}
	public void setBook_allnum(int book_allnum) {
		Book_allnum = book_allnum;
	}
	public int getBook_surplus() {
		return Book_surplus;
	}
	public void setBook_surplus(int book_surplus) {
		Book_surplus = book_surplus;
	}
	
}
